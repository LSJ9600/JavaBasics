package com.syntax.class01;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		//storing and representing whole numbers values;

		byte box1 = 127;
		
		short box2 = 32767; 
		
		int box3 = 100000; //popular; 
		
		long box4 = 5759274893240893l; // credit card, transaction id;
		
		
		//storing and representing decimal values;
		float variable1= 1.99F;
		
		double variable2 = 99.99; //popular; 
		
		//storing single characters
		
		char container= 'a';
		char dollar= '$';
		
		//storing yes or no values
		
		boolean hot= true;
		boolean tired= false;
		
		System.out.println(variable2); //printing value of the variable2
		System.out.println("variable2"); //printing text variable2 itself
		
		System.out.println(box1);
		System.out.println(box2);
		System.out.println(box3);
		System.out.println(box4);
		System.out.println(variable1);
		System.out.println(variable2);
		System.out.println(container);
		System.out.println(dollar);
		System.out.println(hot);
		System.out.println(tired);
		
	}

}
