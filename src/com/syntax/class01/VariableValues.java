package com.syntax.class01;

public class VariableValues {

	public static void main(String[] args) {

		//storing and representing whole numbers values;

				byte store1 = 100;
				
				short store2 = 3098; 
				
				int store3 = 4320947; //popular; 
				
				long store4 = 8472309847032874L; // credit card, transaction id;
				
				
				//storing and representing decimal values;
				float water1= 4.5F;
				
				double boat2 = 54.09; //popular; 
				
				//storing single characters
				
				char lid= 'X';
				char cap= '$';
				
				//storing yes or no values
				
				boolean wet= true;
				boolean dry= false;

		
				System.out.println(store1);
				System.out.println(store2);
				System.out.println(store3);
				System.out.println(store4);
				System.out.println(water1);
				System.out.println(boat2);
				System.out.println(lid);
				System.out.println(cap);
				System.out.println(wet);
				System.out.println(dry);
	}

}
